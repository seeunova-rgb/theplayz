function getSnowSpawn() {
  // จุด spawn ที่ปลอดภัย — อยู่นอก fort ทุกหลัง
  const safePoints = [
    { x: 800,  y: 1600 }, { x: 1600, y: 800  },
    { x: 5200, y: 800  }, { x: 5200, y: 1600 },
    { x: 800,  y: 4400 }, { x: 1600, y: 5200 },
    { x: 5200, y: 4400 }, { x: 4400, y: 5200 },
    { x: 3000, y: 800  }, { x: 800,  y: 3000 },
    { x: 5200, y: 3000 }, { x: 3000, y: 5200 },
  ];
  return safePoints[Math.floor(Math.random() * safePoints.length)];
}

