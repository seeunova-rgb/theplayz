// เรียกจาก game.js แทน CONFIG.WORLD/2
function getAirportSpawn() {
  const s = AIRPORT_SAFE_SPAWNS[Math.floor(Math.random() * AIRPORT_SAFE_SPAWNS.length)];
  // สุ่ม jitter ±80px เพื่อไม่ให้ผู้เล่นหลายคน spawn ซ้อนกัน
  return {
    x: s.x + (Math.random() - 0.5) * 160,
    y: s.y + (Math.random() - 0.5) * 160,
  };
}

